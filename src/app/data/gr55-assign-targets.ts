/**
 * Returns the display label for a given target value.
 * Includes the numeric index prefix matching the GR-55 display.
 */
export function getAssignTargetLabel(value: number): string {
  if (value < 0 || value >= GR55_ASSIGN_TARGETS.length) {
    return `Unknown (${value})`;
  }
  return GR55_ASSIGN_TARGETS[value];
}

/**
 * Returns the group name for a given target value.
 */
export function getAssignTargetGroup(value: number): string {
  for (const group of GR55_ASSIGN_TARGET_GROUPS) {
    if (value >= group.start && value <= group.end) {
      return group.label;
    }
  }
  return 'Unknown';
}
