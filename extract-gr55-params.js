function classifyParameter(name) {
  // Determine UX level
  let uiLevel = 'advanced';
  if (UX_RULES.primary.includes(name)) {
    uiLevel = 'primary';
  } else if (UX_RULES.secondary.some(pattern => name.includes(pattern))) {
    uiLevel = 'secondary';
  } else if (UX_RULES.secondary.includes(name)) {
    uiLevel = 'secondary';
  }
  
  // Determine category
  let category = 'Other';
  for (const [pattern, cat] of Object.entries(CATEGORIES)) {
    if (name.toLowerCase().includes(pattern.toLowerCase())) {
      category = cat;
      break;
    }
  }
  
  return { uiLevel, category };
}

